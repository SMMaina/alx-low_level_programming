#include <stdio.h>

/**
 * add - addition
 * @x: int first
 * @y: int second
 * Return: int
 */
int add(int x, int y)
{
	return (x + y);
}
/**
 * sub - minuses
 * @x: int first
 * @y: int secnd
 * Return: int
 */
int sub(int x, int y)
{
	return (x - y);
}
/**
 * mul - multiplies
 * @x: int first
 * @y: int second
 * Return: int
 */
int mul(int x, int y)
{
	return (x * y);
}
/**
 * div - checks division
 * @x: int first
 * @y: int second
 * Return: int
 */
int div(int x, int y)
{
	return (x / y);
}
/**
 * mod - checks module
 * @x: int first
 * @y: int next
 * Return: int
 */
int mod(int x, int y)
{
	return (x % y);
}
