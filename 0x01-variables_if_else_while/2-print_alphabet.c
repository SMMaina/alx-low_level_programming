#include <stdio.h>
/**
 * main - Entry point
 * main code contains the source code
 *
 * Return: return 0 always if success
*/
int main(void)
{
	int ch;
	/* print alphabet in lower case */
for (ch = 'a'; ch <= 'z'; ch++)
		putchar(ch);
	putchar('\n');
	/* return which offer 0 if file is correct */
return (0);
}
